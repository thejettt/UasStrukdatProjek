    private void btnok1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnok1ActionPerformed
        if (Kode_Barang.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Kode Barang Belum Diisi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else if (Kode_Barang4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Jumlah Barang Belum Diisi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Barang barangKeluar = inv.removeBarang(Integer.parseInt(Kode_Barang4.getText()), Integer.parseInt(Kode_Barang5.getText()));
            if (barangKeluar != null) {
                System.out.println("Barang keluar dari inventori: " + barangKeluar.getNamaBarang());
                JOptionPane.showMessageDialog(null, "Barang berhasil dikurangkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                pan_barangKeluar.setVisible(false);
                pan_barangMasuk.setVisible(false);
                pan_tampilkanBarang.setVisible(true);
                jLabel9.setVisible(false);
                display();
            }
        }
    }//GEN-LAST:event_btnok1ActionPerformed

    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        String keyword = jTextField1.getText().trim(); // Ambil string pencarian
        tabmode.setRowCount(0); // Kosongkan tabel terlebih dahulu

        if (keyword.isEmpty()) {
            display(); // Tampilkan semua barang jika tidak ada kata kunci
        } else {
            for (Barang barang : inv.getAllSortedBarang()) {
                if (barang.getNamaBarang().toLowerCase().contains(keyword.toLowerCase())) { // Cek apakah nama barang mengandung kata kunci (case-insensitive)
                    Object[] row = new Object[4];
                    row[0] = barang.getIdBarang();
                    row[1] = barang.getNamaBarang();
                    row[2] = barang.getTanggalMasuk();
                    row[3] = barang.getJumlahBarang();
                    tabmode.addRow(row);
                }
            }
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

