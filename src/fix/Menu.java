    private void btnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokActionPerformed
        Date tanggal = null;
        try {
            tanggal = new SimpleDateFormat("dd-MM-yyyy").parse(Kode_Barang2.getText());
        } catch (ParseException e) {
            e.printStackTrace();
        }
        if (Kode_Barang.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Kode Barang Belum Diisi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else if (Kode_Barang1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Nama Barang Belum Diisi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else if (Kode_Barang2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Tanggal Masuk Belum Diisi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else if (Kode_Barang3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Jumlah Barang Belum Diisi", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else if (tanggal == null) {
            JOptionPane.showMessageDialog(null, "Format Tanggal tidak sesuai dd-MM-yyyy", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Barang barangBaru = new Barang(Integer.parseInt(Kode_Barang.getText()), Kode_Barang1.getText(), tanggal, Integer.parseInt(Kode_Barang3.getText()));
            inv.insertBarang(barangBaru);
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            pan_barangKeluar.setVisible(false);
            pan_barangMasuk.setVisible(false);
            pan_tampilkanBarang.setVisible(true);
            jLabel9.setVisible(false);
            display();
        }
    }//GEN-LAST:event_btnokActionPerformed

